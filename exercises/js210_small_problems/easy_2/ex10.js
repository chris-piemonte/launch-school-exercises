function century(year) {
  const century = Math.ceil(year / 100)
  let suffix = century % 100

  if (suffix > 13) suffix %= 10;

  switch (suffix) {
    case 1:
      suffix = 'st';
      break;
    case 2:
      suffix = 'nd';
      break;
    case 3:
      suffix = 'rd';
      break;
    default:
      suffix = 'th';
  }

  return String(century) + suffix;
}

console.log(century(2000));        // "20th"
console.log(century(2001));        // "21st"
