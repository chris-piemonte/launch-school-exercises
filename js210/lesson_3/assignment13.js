function generatePattern(num) {
  let array = []

  for (let i = 1; i <= num; i += 1) {
    array.push(String(i));
  }

  let length = array.join('').length;

  let currentNum = ''
  array.forEach(function(num) {
    currentNum += num
    console.log(`${currentNum}${'*'.repeat(length - currentNum.length)}`);
  });
}

generatePattern(20);

